#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s;
	int		i;
	int		length;

	i = 0;
	length = 0;
	while (s1[i])
	{
		length++;
		i++;
	}
	if ((s = malloc(sizeof(char) * (length + 1))) == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		s[i] = s1[i];
		i++;
	}
	s[i] = '\0';
	return (s);
}
