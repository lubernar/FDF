#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;
	int occ;

	i = 0;
	occ = -1;
	if (c == '\0')
		return ((char*)&s[ft_strlen(s)]);
	while (s[i])
	{
		if (s[i] == c)
			occ = i;
		i++;
	}
	if (occ == -1)
		return (NULL);
	return ((char*)&s[occ]);
}
